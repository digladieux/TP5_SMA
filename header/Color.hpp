#ifndef COLOR_HPP
#define COLOR_HPP

/**
  * \def RESET
  * \brief Remise a 0 de la couleur du terminal
 */
#define RESET   "\033[0m"

/**
  * \def BLACK
  * \brief Couleur noir pour le terminale
 */
#define BLACK   "\033[30m"   

/**
  * \def RED
  * \brief Couleur rouge pour le terminale
 */
#define RED     "\033[31m"    

/**
  * \def GREEN
  * \brief Couleur verte pour le terminale
 */
#define GREEN   "\033[32m"      

/**
  * \def YELLOW
  * \brief Couleur jaune pour le terminale
 */
#define YELLOW  "\033[33m"   

/**
  * \def BLUE
  * \brief Couleur bleue pour le terminale
 */
#define BLUE    "\033[34m"   

/**
  * \def MAGENTA
  * \brief Couleur violette pour le terminale
 */
#define MAGENTA "\033[35m" 

/**
  * \def CYAN
  * \brief Couleur cyan pour le terminale
 */
#define CYAN    "\033[36m" 

/**
  * \def WHITE
  * \brief Couleur blanche pour le terminale
 */
#define WHITE   "\033[37m"

/**
  * \def BOLDBLACK
  * \brief Couleur noir (gras) pour le terminale
 */
#define BOLDBLACK   "\033[1m\033[30m"

/**
  * \def BOLDRED
  * \brief Couleur rouge (gras) pour le terminale
 */
#define BOLDRED     "\033[1m\033[31m"      

/**
  * \def BOLDGREEN
  * \brief Couleur verte (gras) pour le terminale
 */
#define BOLDGREEN   "\033[1m\033[32m"      

/**
  * \def BOLDYELLOW
  * \brief Couleur jaune (gras) pour le terminale
 */
#define BOLDYELLOW  "\033[1m\033[33m" 

/**
  * \def BOLDBLUE
  * \brief Couleur bleue (gras) pour le terminale
 */
#define BOLDBLUE    "\033[1m\033[34m"      

/**
  * \def BOLDMAGENTA
  * \brief Couleur violette (gras) pour le terminale
 */
#define BOLDMAGENTA "\033[1m\033[35m"      

/**
  * \def BOLDCYAN
  * \brief Couleur cyan (gras) pour le terminale
 */
#define BOLDCYAN    "\033[1m\033[36m"      

/**
  * \def BOLDWHITE
  * \brief Couleur blanche (gras) pour le terminale
 */
#define BOLDWHITE   "\033[1m\033[37m"      
#endif
